import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const svgDir = join(__dirname, "../public/svg/carcas-original");
const outFile = join(__dirname, "../src/data/carcas-zone-data.ts");

const UPPER_ZONE_NUMBERS = [1, 2, 3, 4, 5, 6, 7, 9];
const LOWER_ZONE_NUMBERS = [8, 10, 11, 12, 13];

function parseSvgFile(filePath) {
  const content = readFileSync(filePath, "utf8");
  const viewBoxMatch = content.match(/viewBox="([^"]+)"/);
  const transformMatch = content.match(/transform="([^"]+)"/);
  const pathMatch = content.match(/\bd="([^"]+)"/);

  if (!viewBoxMatch || !transformMatch || !pathMatch) {
    throw new Error(`Could not parse ${filePath}`);
  }

  return {
    viewBox: viewBoxMatch[1],
    transform: transformMatch[1],
    path: pathMatch[1],
  };
}

function getPathPoints(d) {
  const points = [];
  let cx = 0;
  let cy = 0;
  let sx = 0;
  let sy = 0;

  const commands =
    d.trim().match(/[MmLlHhVvCcSsQqTtAaZz][^MmLlHhVvCcSsQqTtAaZz]*/g) ?? [];

  for (const command of commands) {
    const type = command[0];
    const nums = (command.slice(1).match(/-?\d*\.?\d+(?:e-?\d+)?/gi) ?? []).map(
      Number
    );

    const addPoint = (x, y) => {
      points.push([x, y]);
      cx = x;
      cy = y;
    };

    switch (type) {
      case "M":
        for (let i = 0; i < nums.length; i += 2) addPoint(nums[i], nums[i + 1]);
        sx = cx;
        sy = cy;
        break;
      case "m":
        for (let i = 0; i < nums.length; i += 2)
          addPoint(cx + nums[i], cy + nums[i + 1]);
        sx = cx;
        sy = cy;
        break;
      case "L":
        for (let i = 0; i < nums.length; i += 2) addPoint(nums[i], nums[i + 1]);
        break;
      case "l":
        for (let i = 0; i < nums.length; i += 2)
          addPoint(cx + nums[i], cy + nums[i + 1]);
        break;
      case "C":
        for (let i = 0; i < nums.length; i += 6) {
          addPoint(nums[i + 4], nums[i + 5]);
        }
        break;
      case "c":
        for (let i = 0; i < nums.length; i += 6) {
          addPoint(cx + nums[i + 4], cy + nums[i + 5]);
        }
        break;
      case "Z":
      case "z":
        addPoint(sx, sy);
        break;
      default:
        break;
    }
  }

  return points;
}

function getLabelPosition(path) {
  const pts = getPathPoints(path);
  const xs = pts.map((p) => p[0]);
  const ys = pts.map((p) => p[1]);
  return {
    x: Number(((Math.min(...xs) + Math.max(...xs)) / 2).toFixed(1)),
    y: Number(((Math.min(...ys) + Math.max(...ys)) / 2).toFixed(1)),
  };
}

function buildPartConfig(zoneNumbers, referenceZone) {
  const reference = parseSvgFile(join(svgDir, `${referenceZone}.svg`));
  const paths = {};
  const labels = {};

  for (const zone of zoneNumbers) {
    const { path } = parseSvgFile(join(svgDir, `${zone}.svg`));
    const letter = String.fromCharCode(64 + zone);
    const { x, y } = getLabelPosition(path);
    paths[zone] = path;
    labels[zone] = { letter, x, y };
  }

  const [vbW, vbH] = reference.viewBox.split(" ").slice(2).map(Number);

  return {
    viewBox: reference.viewBox,
    transform: reference.transform,
    aspectRatio: { width: vbW, height: vbH },
    paths,
    labels,
  };
}

const upper = buildPartConfig(UPPER_ZONE_NUMBERS, 1);
const lower = buildPartConfig(LOWER_ZONE_NUMBERS, 8);

function formatPaths(paths) {
  return Object.entries(paths)
    .map(([n, d]) => `    ${n}: ${JSON.stringify(d)},`)
    .join("\n");
}

function parseExistingLabels(existingLabelsContent) {
  const parsed = {};

  for (const match of existingLabelsContent.matchAll(
    /(\d+): \{ letter: "([^"]+)", x: ([\d.]+), y: ([\d.]+)(?:, fontSize: (\d+))?\s*\}/g
  )) {
    const [, zone, letter, x, y, fontSize] = match;
    parsed[zone] = {
      letter,
      x: Number(x),
      y: Number(y),
      ...(fontSize ? { fontSize: Number(fontSize) } : {}),
    };
  }

  return parsed;
}

function formatLabels(labels, existingLabelsContent) {
  const existingLabels = parseExistingLabels(existingLabelsContent);

  return Object.entries(labels)
    .map(([n, computed]) => {
      const saved = existingLabels[n];
      const letter = saved?.letter ?? computed.letter;
      const x = saved?.x ?? computed.x;
      const y = saved?.y ?? computed.y;
      const fontSizePart = saved?.fontSize ? `, fontSize: ${saved.fontSize}` : "";
      return `    ${n}: { letter: "${letter}", x: ${x}, y: ${y}${fontSizePart} },`;
    })
    .join("\n");
}

let existingLabels = "";
try {
  existingLabels = readFileSync(outFile, "utf8");
} catch {
  existingLabels = "";
}

const output = `// Auto-generated by scripts/sync-carcas-zones.mjs — do not edit manually.
// Re-run: node scripts/sync-carcas-zones.mjs

export const UPPER_ZONE_NUMBERS = [${UPPER_ZONE_NUMBERS.join(", ")}] as const;
export const LOWER_ZONE_NUMBERS = [${LOWER_ZONE_NUMBERS.join(", ")}] as const;

export type CarcasZoneNumber =
  | (typeof UPPER_ZONE_NUMBERS)[number]
  | (typeof LOWER_ZONE_NUMBERS)[number];

export const ALL_ZONE_NUMBERS: readonly CarcasZoneNumber[] = [
  ...UPPER_ZONE_NUMBERS,
  ...LOWER_ZONE_NUMBERS,
].sort((a, b) => a - b);

export type CarcasPart = "upper" | "lower" | "whole";

export function getZonesForPart(part: CarcasPart): CarcasZoneNumber[] {
  if (part === "upper") return [...UPPER_ZONE_NUMBERS];
  if (part === "lower") return [...LOWER_ZONE_NUMBERS];
  return [...ALL_ZONE_NUMBERS];
}

export function isZoneInPart(
  zoneNumber: number,
  part: CarcasPart
): zoneNumber is CarcasZoneNumber {
  return getZonesForPart(part).includes(zoneNumber as CarcasZoneNumber);
}

export type CarcasZoneLabel = {
  letter: string;
  x: number;
  y: number;
  fontSize?: number;
};

export const CARCAS_ZONE_LABEL_FONT_SIZE_DEFAULT = 11;

export type CarcasPartConfig<Z extends CarcasZoneNumber = CarcasZoneNumber> = {
  viewBox: string;
  transform: string;
  aspectRatio: { width: number; height: number };
  zoneNumbers: readonly Z[];
  paths: Record<Z, string>;
  labels: Record<Z, CarcasZoneLabel>;
};

export const CARCAS_UPPER_CONFIG: CarcasPartConfig<
  (typeof UPPER_ZONE_NUMBERS)[number]
> = {
  viewBox: "${upper.viewBox}",
  transform: "${upper.transform}",
  aspectRatio: { width: ${upper.aspectRatio.width}, height: ${upper.aspectRatio.height} },
  zoneNumbers: UPPER_ZONE_NUMBERS,
  paths: {
${formatPaths(upper.paths)}
  },
  labels: {
${formatLabels(upper.labels, existingLabels)}
  },
};

export const CARCAS_LOWER_CONFIG: CarcasPartConfig<
  (typeof LOWER_ZONE_NUMBERS)[number]
> = {
  viewBox: "${lower.viewBox}",
  transform: "${lower.transform}",
  aspectRatio: { width: ${lower.aspectRatio.width}, height: ${lower.aspectRatio.height} },
  zoneNumbers: LOWER_ZONE_NUMBERS,
  paths: {
${formatPaths(lower.paths)}
  },
  labels: {
${formatLabels(lower.labels, existingLabels)}
  },
};

export const CARCAS_PART_CONFIG = {
  upper: CARCAS_UPPER_CONFIG,
  lower: CARCAS_LOWER_CONFIG,
} as const;
`;

writeFileSync(outFile, output);
console.log(`Wrote ${outFile}`);
